const inNavigationTarget = (capability: Capability, target: CapabilityNavigationTarget) =>
  capability.navigation.targets.includes(target);

export const availableCapabilities = capabilities.filter((capability) => capability.status === "available");

export const capabilityById = (id: string) => capabilities.find((capability) => capability.id === id);

export const capabilitiesIn = (
  domain: CapabilityDomain,
  status: CapabilityStatus = "available",
  target: CapabilityNavigationTarget = "domain",
) =>
  capabilities
    .filter(
      (capability) =>
        capability.domain === domain && capability.status === status && inNavigationTarget(capability, target),
    )
    .sort((a, b) => a.navigation.order - b.navigation.order);

export const workspaceCapabilitiesIn = (domain: CapabilityDomain, status: CapabilityStatus = "available") =>
  capabilitiesIn(domain, status, "workspace");
