export type CapabilityStatus = "available" | "planned" | "external";

export type CapabilityDomain = "home" | "catalog" | "tools" | "community" | "admin" | "rankings" | "games" | "profile";

export type CapabilityKind =
  "workspace" | "catalog" | "detail" | "tool" | "studio" | "runtime" | "account" | "external";

export type CapabilityAccessLevel = "public" | "browser-local" | "identity" | "verified-game" | "moderator";
export type CapabilityRegion = "global" | "jp" | "en" | "tw" | "cn" | "kr";
export type CapabilityNavigationTarget = "shell" | "domain" | "workspace";
export type CapabilityDataSource = "catalog" | "media" | "browser" | "worker" | "d1" | "r2-documents" | "external";
export type CapabilityOperation =
  | "read"
  | "search"
  | "filter"
  | "sort"
  | "play"
  | "preview"
  | "download"
  | "calculate"
  | "simulate"
  | "track"
  | "create"
  | "edit"
  | "revise"
  | "publish"
  | "comment"
  | "react"
  | "follow"
  | "import"
  | "export"
  | "sync"
  | "submit"
  | "manage"
  | "moderate";

export interface CapabilityRoutes {
  detail?: string | readonly string[];
  search?: string;
  create?: string;
  edit?: string;
  revision?: string;
}

export interface CapabilityAccess {
  read: CapabilityAccessLevel;
  write?: CapabilityAccessLevel;
  participate?: CapabilityAccessLevel;
}

export interface CapabilityScale {
  cardinality: "bounded" | "catalog" | "stream";
  transport: "inline" | "sharded" | "cursor";
  render: "static" | "windowed" | "paged";
}

export interface CapabilityNavigation {
  order: number;
  targets: readonly CapabilityNavigationTarget[];
}

export interface Capability {
  id: string;
  domain: CapabilityDomain;
  label: string;
  route: string;
  routes?: CapabilityRoutes;
  status: CapabilityStatus;
  kind: CapabilityKind;
  access: CapabilityAccess;
  regions: readonly CapabilityRegion[];
  scale: CapabilityScale;
  operations: readonly CapabilityOperation[];
  data: readonly CapabilityDataSource[];
  navigation: CapabilityNavigation;
}

type CapabilityInput = Omit<Capability, "access" | "regions" | "scale" | "data"> &
  Partial<Pick<Capability, "access" | "regions" | "scale" | "data">>;

